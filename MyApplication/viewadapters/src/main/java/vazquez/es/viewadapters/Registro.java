public class Registro {
    private String uno;
    private String dos;

    public Registro() {
        super();
    }

    public Registro(String uno, String dos) {
        super();
        this.uno = uno;
        this.dos = dos;
    }

    public String getUno() {
        return uno;
    }

    public void setUno(String uno) {
        this.uno = uno;
    }

    public String getDos() {
        return dos;
    }

    public void setDos(String dos) {
        this.dos = dos;
    }
}


